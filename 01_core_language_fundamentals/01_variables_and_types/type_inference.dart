/*
========================================
TOPIC NAME:
Type Inference
========================================

DEFINITION:
----------------------------------------
Type inference means Dart automatically
figures out the data type of a variable
based on the assigned value.

CONCEPT + REAL LIFE EXAMPLE:
----------------------------------------
Jab hum var use karte hain, Dart khud
samajh leta hai ke variable ka type kya hai.

Real life example:
Jaise hum kisi ko dekh kar keh dete hain
yeh student hai ya teacher, bina poochay.

SYNTAX:
----------------------------------------
var name = "Ali";
var age = 20;
*/

// ================================
// PROGRAM CODE
// ================================

void main() {
  var name = "Ali";
  var age = 20;
  var price = 99.5;

  print(name);
  print(age);
  print(price);
}

/*
OUTPUT:
----------------------------------------
Ali
20
99.5
========================================
*/
